pub fn my_first_interpreter(code: &str) -> String {
    let mut res = String::new();
    code.to_string().chars().fold(0u8, |mut state, c|{
        match c {
        '+' => { state = state.wrapping_add(1) }
        '.' => { res.push(state as char) }
         _  => {}
        }
        state
    });
    return res
}

#[cfg(test)]
